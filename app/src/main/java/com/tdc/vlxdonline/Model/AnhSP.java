package com.tdc.vlxdonline.Model;

public class AnhSP {
    public String anhSP,id;

    public AnhSP() {
    }

    public AnhSP(String anhSP, String id) {
        this.anhSP = anhSP;
        this.id = id;
    }

    public String getAnhSP() {
        return anhSP;
    }

    public void setAnhSP(String anhSP) {
        this.anhSP = anhSP;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
